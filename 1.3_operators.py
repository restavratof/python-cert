# division
print('-'*3,'/')
print(14/4)

# result of integer division without reminder left
print('-'*3,'//')
print(14//4)
print(-6//4)
print(6.//-4)

# remainder left after the integer division
print('-'*3,'%')
print(14%4)

print('-'*3,'+')
print(-4 + 4)
print(-4. + 8)

